#pragma once
#include <vector>


class PostfixAndPrefixClass
{
private:
	struct PrePostFix
	{
		char* prefix;
		char* postfix;
		bool numbering;
		PrePostFix() {
			prefix = NULL;
			postfix = NULL;
			numbering = NULL;
		}
	};

	std::vector <std::string> CaptionsVec =
	{
		{
		{"Enter Prefix : "},
		{"Enter Postfix: "},
		{ "Need line numbering?: y/n: "},
		{"Notice, string is empty" }
		}
	};

public:
	PrePostFix* prePostFix;
	PostfixAndPrefixClass()
	{
		prePostFix = NULL;
	}
	PrePostFix* prePostFixVoid();
	~PostfixAndPrefixClass();
};






















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































